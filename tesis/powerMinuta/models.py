from django.db import models

# Create your models here.
class Nutricionista(models.Model):
    rut_nutricionista = models.IntegerField(primary_key=True)
    nombre_nutricionista = models.CharField(max_length=25)
    apellido_nutricionista = models.CharField(max_length=25)
    correo_nutricionista = models.CharField(max_length=50)
    entidad_nutricionista = models.CharField(max_length=25)
    id_nutricionista = models.CharField(max_length=25)
    contrasena_nutricionista = models.CharField(max_length=8)

class Paciente(models.Model):
    rut_paciente = models.IntegerField(primary_key=True)
    rut_nutricionista = models.ForeignKey(Nutricionista)
    nombre_paciente = models.CharField(max_length=25)
    apellido_paciente = models.CharField(max_length=25)
    edad_paciente = models.IntegerField()
    genero_paciente = models.IntegerField()

class Ficha(models.Model):
    codigo_ficha = models.IntegerField(primary_key=True)
    rut_paciente = models.ForeignKey(Paciente)
    peso_ficha = models.DecimalField(max_digits=6, decimal_places=3)
