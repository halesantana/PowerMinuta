    circunferenciaCintura_ficha = models.DecimalField(max_digits=6, decimal_places=3)
    circunferenciaBraquial = models.DecimalField(max_digits=6, decimal_places=3)
    pliegueSubEscapular = models.DecimalField(max_digits=6, decimal_places=3)
    pliegueTricipital = models.DecimalField(max_digits=6, decimal_places=3)
    pliegueBicipital = models.DecimalField(max_digits=6, decimal_places=3)
    pliegueSupraIliaco = models.DecimalField(max_digits=6, decimal_places=3)
    indiceMasaCorporal_ficha = models.IntegerField()
    riesgoCardioVascular_ficha = models.IntegerField()
    antecedentesPatologicos_ficha = models.TextField()
